package com.mycompany.u2.e26.pruebatablamulti9;

public class PruebaTablaMulti9 {
    public static void main(String[] args) {
        int num=9;
        int variable=0;
         
        while(variable<=10){
            int resultado=num*variable;
            System.out.println(variable+"x"+num+" = "+resultado);
            variable++;
        }
    }
}
